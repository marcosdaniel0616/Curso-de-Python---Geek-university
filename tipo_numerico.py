"""
Tipo numérico
"""
# você pode usar um _ (underline) para separar milhares em Python
num = 1_000_000
# soma
print(1 + 1)
num += 1
# subtração
print(5 - 3)
num -= 1
# multiplicação
print(7 * 5)
num *= 2
# divisão
print(8 / 2)
num /= 2
# divisão inteira
print(10 // 6)
num //= 2
# elevação
print(5 ** 3)
num **= 2
# resto da divisão
print(5 % 2)
